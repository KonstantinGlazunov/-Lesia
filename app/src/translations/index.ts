function getNested(obj: Record<string, unknown>, path: string): string | undefined {
  // Flat key (e.g. 'nav.about' stored as single key)
  const direct = (obj as Record<string, string>)[path];
  if (typeof direct === 'string') return direct;
  const value = path.split('.').reduce((acc: unknown, key) => (acc as Record<string, unknown>)?.[key], obj);
  return typeof value === 'string' ? value : undefined;
}

export function t(locale: Locale, key: string): string {
  const value = getNested(translations[locale] as Record<string, unknown>, key);
  if (value) return value;
  const fallback = getNested(translations.ru as Record<string, unknown>, key);
  return fallback ?? key;
}
